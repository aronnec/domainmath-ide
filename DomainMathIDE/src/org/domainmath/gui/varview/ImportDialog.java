    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {
           file = new File(this.fileNameField.getText()) ;
            String sheet = this.worsheetNameField.getText();
            String xls = "xlsread ('"+file.getAbsolutePath()+"','"+sheet+"');";
            String ods = "odsread ('"+file.getAbsolutePath()+"','"+sheet+"');";
            String fname=file.getName();
            if(fname.endsWith(".xlsx")) {
                 ImportVarDialog dlg = new ImportVarDialog(frame,true,xls);
                 dlg.setLocationRelativeTo(frame);
                 dlg.setVisible(true);
                  
            }else if(fname.endsWith(".ods")) {
                 ImportVarDialog dlg = new ImportVarDialog(frame,true,ods);
                 dlg.setLocationRelativeTo(frame);
                 dlg.setVisible(true);
            }
            dispose();
        }catch(Exception e) {
            JOptionPane.showMessageDialog(frame,"Insufficient Input.");
            dispose();
        }
    }//GEN-LAST:event_okButtonActionPerformed

